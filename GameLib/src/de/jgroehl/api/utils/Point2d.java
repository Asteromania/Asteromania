package de.jgroehl.api.utils;

/**
 * 
 * @author Janek Gröhl
 *
 */
public class Point2d
{

	private final float x;
	private final float y;

	public Point2d(float x, float y)
	{
		this.x = x;
		this.y = y;
	}

	public float getX()
	{
		return x;
	}

	public float getY()
	{
		return y;
	}
}
